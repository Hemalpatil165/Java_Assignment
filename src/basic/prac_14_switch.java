package basic;

public class prac_14_switch
{
	public static void main(String[] args) 
	{
		int no=1;
		
		switch (no) {
		case 1:
			System.out.println("sunday");
			break;
		case 2:
			System.out.println("mon");
			break;
		case 3:
			System.out.println("tues");
			break;
			
			
			
			
			
			
			
			

		default:
			break;
		}
		
	}

}
