package basic;

public class jk
{	
//	    *
//	   ***
//	  *****
	public static void main(String[] args) 
	{
	 for(int i=1; i<=3; i++)
	 {
		 for(int k=i; k<=3; k++) {
			 System.out.print(" ");
		 }
		 for(int j=1; j<=i*2-1; j++)
		 {
			 System.out.print("*");
		 }
		 System.out.println();
	 }
		
	
	for(int l=1; l<=3; l++)
	 {
		 for(int m=l; m<=3; m++) {
			 System.out.print(" ");
		 }
		 for(int j=1; j<=l*2-1; j++)
		 {
			 System.out.print("*");
		 }
		 System.out.println();
	
}
}
}
