package basic;

public class Prac_2ins_value 
{
	public static void main(String[] args)
	{
		int i1 = 2147483647;
		long l1 = 9856985625985l;
		char ch = 'H';
		boolean b1 = true;
		byte bt = 127;
		float f1 = 98.90f;
		double d1 = 898989.7788;
		
		System.out.println("int value is= " + i1);
		System.out.println("long value is= " + l1);
		System.out.println("char value is= " + ch);
		System.out.println("boolean value is= " + b1);
		System.out.println("byte value is= " + bt);
		System.out.println("float value is= " + f1);
		System.out.println("double value is= " + d1);
	
		
	}

}
